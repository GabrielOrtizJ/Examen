class Planeta {

    var nombre =""
    var tipoPlaneta=""
    var descubrimiento=""
    var numeroSatelites=""
    var presenciaAnillos=""

    constructor(nombre:String, tipoPlaneta:String,descubrimiento:String, numeroSatelites:String ,presenciaAnillos:String){

        this.nombre = nombre
